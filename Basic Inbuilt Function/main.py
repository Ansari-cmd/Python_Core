# Basic Arithematic Function
x = 3.14
y = 4
z = 5

result = round(x)  # 3
print(result)
result = abs(y)  # -4 to 4
print(result)
result = pow(4,3)  # 4x4x4 = 64
print(result)
result = max(x,y,z)  # 5
print(result)
result = min(x,y,z)  # 3.14
print(result)

